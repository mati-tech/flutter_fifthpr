class User {
  final int id;
  final String email;
  final String username;
  final String password;
  final String firstname;
  final String lastname;
  final String phone;

  User({
    required this.id,
    required this.email,
    required this.username,
    required this.password,
    required this.firstname,
    required this.lastname,
    required this.phone,
  });
}